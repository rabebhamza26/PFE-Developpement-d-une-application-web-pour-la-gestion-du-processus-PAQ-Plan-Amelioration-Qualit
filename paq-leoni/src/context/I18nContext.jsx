export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem("lang") || "fr");

  useEffect(() => {
    localStorage.setItem("lang", lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  }, [lang]);

  const t = useMemo(() => {
    return (key, fallbackOrParams) => {
      const entry = DICTIONARY[lang]?.[key];
      let translation = entry ?? (typeof fallbackOrParams === "string" ? fallbackOrParams : key);

      if (translation && fallbackOrParams && typeof fallbackOrParams === "object") {
        Object.entries(fallbackOrParams).forEach(([paramKey, paramValue]) => {
          translation = translation.replace(new RegExp(`\\{\\{${paramKey}\\}\\}`, "g"), String(paramValue));
        });
      }

      return translation;
    };
  }, [lang]);

  const value = useMemo(() => ({ lang, setLang, t }), [lang, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n doit être utilisé dans I18nProvider");
  return ctx;
}
